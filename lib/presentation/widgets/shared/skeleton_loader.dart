        border: Border.all(color: Colors.grey[200]!),
      ),
      child: const Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          SkeletonLoader(width: 50, height: 50, borderRadius: 25),
          SizedBox(height: 12),
          SkeletonLoader(width: 80, height: 15),
          SizedBox(height: 8),
          SkeletonLoader(width: 60, height: 10),
        ],
      ),
    );
  }
}
